public class CommonProblemBean {
    private String title;
    private int icon;
    private String topProblem;
    private String bottomProblem;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getIcon() {
        return icon;
    }

    public void setIcon(int icon) {
        this.icon = icon;
    }

    public String getTopProblem() {
        return topProblem;
    }

    public void setTopProblem(String topProblem) {
        this.topProblem = topProblem;
    }

    public String getBottomProblem() {
        return bottomProblem;
    }

    public void setBottomProblem(String bottomProblem) {
        this.bottomProblem = bottomProblem;
    }
}
