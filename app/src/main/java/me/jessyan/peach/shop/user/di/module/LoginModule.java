@Module
public abstract class LoginModule {

    @Binds
    abstract LoginContract.Model bindLoginModel(LoginModel model);

    @ActivityScope
    @Provides
    static RxPermissions provideRxPermissions(LoginContract.View view) {
        return new RxPermissions(view.getActivity());
    }
}